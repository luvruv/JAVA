public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello Dhruv...");
        System.out.println("Hello \"Dhruv\"...");
        System.out.println("Hello \\Dhruv\\...");
        System.out.println("Hello \nDhruv...");
        System.out.println("Hello \tDhruv...");
        System.out.println("Hello \bDhruv...");
    }
}
